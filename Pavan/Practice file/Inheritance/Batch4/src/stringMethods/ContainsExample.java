package stringMethods;

public class ContainsExample {

	public static void main(String[] args) {
		String name = "Naresh Kambala";
		String name1 = "Naresh";
		String name2 = "Vikky";
		String name3 = "Vikky";
		System.out.println(name.contains(name1));
		System.out.println(name2.contains(name3));

	}

}
