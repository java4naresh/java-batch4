class OperatorsExample2 {

  public static void main(String[] args){

    int a = 10;
    int b = 10;

    //Arithmetic Operators
     int sum = a + b;//20
     int substraction = a - b;//0
     int mul = a * b;//100
     int coef = a / b;//1
     int rem = a % b;//0
    System.out.println("sum ="+sum);//20
    System.out.println("substraction ="+substraction);//0
    System.out.println("mul ="+mul);//100
    System.out.println("coef ="+coef);//1
    System.out.println("rem ="+rem);//0

    System.out.println((int)'d');//100

    System.out.println('d' + 'd');//200



}


}