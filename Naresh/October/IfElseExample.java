class IfElseExample 
{
	public static void main(String[] args) 
	{
		int num = 6;
		if(num % 2 == 0) {
		System.out.println("Even");
		} else {
		System.out.println("Odd");
		}

		int marks = 34;
		if(marks < 35) {
		System.out.println("Failed");
		} else {
		System.out.println("Passed");
		}

		int date = 27;

		if(date == 27) {
		System.out.println("Valid");
		} else {
		System.out.println("Invalid");
		}
	}
}
