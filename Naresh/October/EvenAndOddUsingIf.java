class EvenAndOddUsingIf 
{
	public static void main(String[] args) 
	{
		int num = 6;

		if(num % 2 == 0) {
		System.out.println(num + " Is Even");
		}

		if(num % 2 != 0) {
		System.out.println(num + " Is Odd");
		}

		// find a given character is ovel or not a,e,i,o,u
		char ch = 'b';

		if(ch == 'a') {
		System.out.println(ch + " Is Ovel");
		}

		if(ch == 'e') {
		System.out.println(ch + " Is Ovel");
		}

		if(ch == 'i') {
		System.out.println(ch + " Is Ovel");
		}

		if(ch == 'o') {
		System.out.println(ch + " Is Ovel");
		}

		if(ch == 'u') {
		System.out.println(ch + " Is Ovel");
		}
	}
}
