package oops;

public class Office {
	
	public String officeName;
	public int id;
	public String address;
	public String branch;
	
	public Office(String officeName, int id, String address, String branch) {
		this.officeName = officeName;
		this.id = id;
		this.address = address;
		this.branch = branch;
	}
	
	

}
